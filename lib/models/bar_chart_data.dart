class CustomBarChartData {
  final String categoria;
  final double realizadoPercentual;
  final double naoRealizadoPercentual;
  final double totalTarefas;
  final String tarefasRealizadas;

  CustomBarChartData({
    required this.categoria,
    required this.realizadoPercentual,
    required this.naoRealizadoPercentual,
    required this.totalTarefas,
    required this.tarefasRealizadas,
  });
}